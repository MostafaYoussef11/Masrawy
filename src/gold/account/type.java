    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        id = txtId.getText();
        name = txtName.getText();
        if(name.length() == 0){
            Tools.MasgBox("البيانات غير مكتملة!!");
        }else{
          String sql = "INSERT INTO _type VALUES ("+id+",'"+name+"');";
          boolean isSave = ConectionDataBase.ExecuteAnyQuery(sql);
          if(isSave){
              Tools.MasgBox("تم الحفظ بنجاح");
              setNew();
          }else{
              Tools.ErorBox("خطأ");
          }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        txtId.setText(table.getValueAt(row, 1).toString());
        txtName.setText(table.getValueAt(row, 0).toString());
        txtId.setEnabled(false);
        txtName.setEnabled(false);
        btnDel.setEnabled(true);
        btnEdit.setEnabled(true);
        btnNew.setEnabled(true);
        btnSave.setEnabled(false);
    }//GEN-LAST:event_tableMouseClicked

