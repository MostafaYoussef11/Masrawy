    private void setnew(){
        String[]coulmnName= new String [] {"الحساب", "المجموعة"};
        ConectionDataBase.fillAndCenterTable("select a.name_account , w.name_workgroup from accountworkgroup aw JOIN workgroup w on aw.id_workgroup =w.id_workgroup JOIN account a ON aw.id_account = a.id_account WHERE aw.id_account ="+savedData.getId_account()+";", table, coulmnName);
       // String groupFirstName = table.getValueAt(0, 1).toString();
        groupFirstName = "اختر المجموعة";
        comGroup.addItem(groupFirstName);
        comGroup.setSelectedItem(groupFirstName);
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(comGroup.getSelectedItem().equals(groupFirstName)){
           Tools.ErorBox("لم يتم اختيار اسم المجموعة");
        }else{
            String id_account = savedData.getId_account();
            String id_workgroup = ConectionDataBase.getIdFrmName("workgroup", comGroup.getSelectedItem().toString());
            String sql = "INSERT INTO accountworkgroup VALUES("+id_account+","+id_workgroup+");";
            boolean isInsert = ConectionDataBase.ExecuteAnyQuery(sql);
            if(isInsert){
              Tools.MasgBox("تم الحفظ");
              setnew();
            }else{
              Tools.ErorBox("خطأ");
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

